export InertiaWheelPendulum, train!, dynamics, dynamics!

const _I1 = 0.0455
const _I2 = 0.00425
const _m3 = 0.183*9.81

struct InertiaWheelPendulum{T,TPROB} <: IDAPBCSystem
    I1::T
    I2::T
    m3::T
    M::Matrix{T}
    p::TPROB
end

function InertiaWheelPendulum(;I1=_I1, I2=_I2, m3=_m3, pretrain=true)
    T = eltype(I1)
    M = diagm([I1, I2])
    pe(q) = m3*(cos(q[1])-one(eltype(q))) 
    H = Hamiltonian(inv(M), pe)
    
    Mdinv = PSDMatrix(T,2,2)
    Vd = NeuralNetwork(T, [2,16,48,1], symmetric=true, fout=x->x.^2, dfout=x->2x)
    Hd = Hamiltonian(Mdinv, Vd)

    input = T[-1.0,1.0]
    input_annihilator = T[1.0 1.0]
    p = IDAPBCProblem(H, Hd, input, input_annihilator)
    init_Md = diagm([0.002,0.002])
    set_constant_Md!(p, p.init_params, init_Md)

    if pretrain
        p.init_params[:] = pretrained_params
    end

    return InertiaWheelPendulum{T,typeof(p)}(I1,I2,m3,M,p)
end

function Base.show(io::IO, sys::InertiaWheelPendulum)
    show(io, sys.p)
end

function train!(sys::InertiaWheelPendulum)
    T = typeof(sys.I1)
    data = Vector{T}[]
    for q1 in range(0, pi/4, step=pi/100)
        for q2 in range(0, pi/2, step=pi/100)
            push!(data, T[q1,q2])
        end
    end
    for q1 in range(0, 2pi, step=pi/50)
        for q2 in range(0, 6pi, step=pi/20)
            push!(data, T[q1,q2])
        end
    end

    loss = PDELossVd(sys.p)
    θ = MLBasedESC.params(sys.p)
    try
        optimize!(loss, θ, data)
    catch e
        !isa(e, InterruptException) && rethrow(e)
        sys.p.init_params[:] = θ
    end
    nothing
end

function dynamics!(sys::InertiaWheelPendulum, Kv=1e-3, b1=0.0, b2=0.0)
    I1,I2,m3,M = (sys.I1, sys.I2, sys.m3, sys.M)
    u = controller(sys.p, damping_gain=Kv)
    f!(dx,x,p,t) = begin
        q1, q2, q1dot, q2dot = x
        qbar = x[1:2]
        momentum = M * [q1dot, q2dot]
        effort = u(qbar,momentum)
        dx[1] = q1dot
        dx[2] = q2dot
        dx[3] = m3*sin(q1)/I1 - effort/I1 - b1/I1*q1dot
        dx[4] = effort/I2 - b2/I2*q2dot
        nothing
    end
end
function dynamics(sys::InertiaWheelPendulum, Kv=1e-3, b1=0.0, b2=0.0)
    I1,I2,m3,M = (sys.I1, sys.I2, sys.m3, sys.M)
    u = controller(sys.p, damping_gain=Kv)
    f(x,p,t) = begin
        dx = similar(x)
        q1, q2, q1dot, q2dot = x
        qbar = x[1:2]
        momentum = M * [q1dot, q2dot]
        effort = u(qbar,momentum)
        dx[1] = q1dot
        dx[2] = q2dot
        dx[3] = m3*sin(q1)/I1 - effort/I1 - b1/I1*q1dot
        dx[4] = effort/I2 - b2/I2*q2dot
        return dx
    end
end

